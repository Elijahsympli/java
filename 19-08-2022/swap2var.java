//Write a Java program to swap two variables.

package com.java;

public class swap2var {
	public static void main(String args[]) {
		int a=5,b=3,c=a;
		System.out.println("Before swapping\n"+"a="+a +" and b="+b);
		a=b;
		b=c;
		System.out.println("Before swapping\n"+"a="+a +" and b="+b);
	}

}
